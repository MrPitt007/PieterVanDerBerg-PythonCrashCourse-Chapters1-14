# Demonstrating integers, exponents, and order of operations

# --- Basic integer operations ---
print("2 + 3 =", 2 + 3)
print("3 - 2 =", 3 - 2)
print("2 * 3 =", 2 * 3)
print("3 / 2 =", 3 / 2)

# --- Exponents ---
print("3 ** 2 =", 3 ** 2)
print("3 ** 3 =", 3 ** 3)
print("10 ** 6 =", 10 ** 6)

# --- Order of operations ---
print("2 + 3 * 4 =", 2 + 3 * 4)
print("(2 + 3) * 4 =", (2 + 3) * 4)

# --- Floats example ---
print("0.1 + 0.6 =", 0.1 + 0.6)
print("3.0 * 2 =", 3.0 * 2)
